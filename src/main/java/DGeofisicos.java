public class DGeofisicos {
    private  String tipo;
    private String fecha;
    private String lugar;
    private int afectados;
    private int impactoEcon;


    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getLugar() {
        return lugar;
    }

    public void setLugar(String lugar) {
        this.lugar = lugar;
    }

    public int getAfectados() {
        return afectados;
    }

    public void setAfectados(int afectados) {
        this.afectados = afectados;
    }

    public int getImpactoEcon() {
        return impactoEcon;
    }

    public void setImpactoEcon(int impactoEcon) {
        this.impactoEcon = impactoEcon;
    }

    public DGeofisicos() {
        this.tipo = tipo;
        this.fecha = fecha;
        this.lugar = lugar;
        this.afectados = afectados;
        this.impactoEcon = impactoEcon;
    }











}
